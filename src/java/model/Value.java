package model;

/**
 *
 * @author ACERR
 */
public class Value {

    private String name;
    private int value;
    private int quantity; // Số lượng bán
    private double revenue; // Doanh thu

    // Constructor, getters và setters
    public Value(String name, int quantity, double revenue) {
        this.name = name;
        this.quantity = quantity;
        this.revenue = revenue;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getRevenue() {
        return revenue;
    }

    public void setRevenue(double revenue) {
        this.revenue = revenue;
    }

    public Value(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

}
